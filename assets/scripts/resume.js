$(document).ready(function(){
    $(".appBtn").click(function(){
        $(".objAbil").hide();
        $(".workHist").removeAttr("hidden");
    })
    $(".eduBtn").click(function(){
        $(".workHist").hide();
        $(".education").removeAttr("hidden");
    })
    $(".refBtn").click(function(){
        $(".education").hide();
        $(".references").removeAttr("hidden");
    })
})
